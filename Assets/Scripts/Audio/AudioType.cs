/// <summary>
/// Enum định nghĩa các loại Audio trong game
/// Dùng để phân nhóm và điều chỉnh volume theo nhóm
/// </summary>
public enum AudioType
{
    SFX,        // Sound Effects (bắn, nổ, va chạm...)
    UI,         // UI Sounds (click, hover...)
    Ambient     // Ambient Sounds (gió, mưa, lửa cháy...)
}
